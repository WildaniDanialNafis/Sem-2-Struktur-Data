public class Nasabah {
    public String norek;
    public String nama;
    public String alamat;
    public int umur;
    public double saldo;

    public Nasabah() {
        
    }

    public Nasabah(String norek, String nama, String alamat, int umur, double saldo) {
        this.norek = norek;
        this.nama = nama;
        this.alamat = alamat;
        this.umur = umur;
        this.saldo = saldo;
    }
}